char	*ft_strupcase(char *str)
{
	char		buff;
	int			i;

	i = 0;
	while (str[i] != 0)
	{
		buff = str[i] - 32;
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = buff;
		i++;
	}
	return (str);
}
