#include <unistd.h>

void	ft_print_alphabet(void)
{
	int		i;
	char	c;

	i = 1;
	c = 97;
	while (i <= 26)
	{
		write(1, &c, 1);
		i = i + 1;
		c = c + 1;
	}
}
